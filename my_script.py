''' Array using numpy module '''
import numpy as np
x=np.array([1,2,3.4,8,12])

# Printing array

print(x)

# Finding mean value from array
mean_val=np.mean(x)

print(mean_val)


# Finding max value from array

max_val=np.max(x)

print(max_val)

# Finding min value from array

min_val=np.min(x)

print(min_val)